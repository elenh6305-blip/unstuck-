/**
 * 任务拆分提示词
 * 将用户输入的任务拆分为适合 ADHD 患者的纳米步骤
 *
 * 可调参数说明：
 *   - 步骤数量：修改"共输出6到10个步骤"
 *   - 步骤时长：修改"30秒到3分钟"
 *   - 步骤字数：修改"不超过20个字"
 */
export const TASK_BREAKDOWN_PROMPT = (taskTitle) => `
你是一位专为ADHD患者拆分任务的治疗师。
将以下任务拆分开，以帮助ADHD患者走出启动困难和"卡住"状态，每一步必须满足：
1. 包含动词（如：打开、找到、点击、写下、复制）
2. 在30秒到3分钟内可独立完成，难度可以非常平缓地逐渐增加
3. 清晰具体
4. 不超过20个字

共输出6到10个步骤。
只返回纯 JSON 数组，不要有任何其他文字或代码块标记：
[{"id":"1","text":"步骤描述"},{"id":"2","text":"步骤描述"}]

任务：${taskTitle}
`.trim();

/**
 * 任务难度分析提示词
 * 判断一个任务步骤是否可以进一步拆解
 *
 * 返回格式：
 *   - canBreakdown: boolean — 是否可以拆解
 *   - steps: Array — 如果可以拆解，返回子步骤数组
 *   - reason: string — 如果不可拆解，描述原因
 */
export const TASK_DIFFICULTY_ANALYSIS_PROMPT = (stepText) => `
你是一位专为ADHD患者拆分任务的治疗师。
用户在执行以下步骤时感到困难，请判断这个步骤是否可以进一步拆分成更小的子步骤。

判断标准：
- 如果步骤包含多个动作（如"打开电脑并登录"），则可以拆解
- 如果步骤描述模糊（如"准备材料"），则可以拆解使其更具体
- 如果步骤已经是一个单一、具体、简单的动作（如"拿起笔""点击按钮"），则不能再拆解

如果可以拆解，将其拆分为3到5个更小的步骤，每步必须:
1. 包含一个明确的动词
2. 在30秒内可完成
3. 不超过15个字

返回纯JSON，格式如下：
如果可以拆解：
{"canBreakdown":true,"steps":[{"id":"1","text":"子步骤描述"},{"id":"2","text":"子步骤描述"}]}
如果不能再拆解：
{"canBreakdown":false,"reason":"已经是最小动作"}

不要有任何其他文字或代码块标记。

步骤内容：${stepText}
`.trim();

